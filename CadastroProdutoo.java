    private void jtxtCodigoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtxtCodigoKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            int codigo = Integer.parseInt(jtxtCodigo.getText());
            String sql = "SELECT * FROM produto WHERE codigo="+codigo+"";
            Conexao con = new Conexao();
            try {
                ResultSet retorno = con.sentenca.executeQuery(sql);
                if(retorno.next()){
                    JOptionPane.showMessageDialog(this, "Produto já existe produto com esse código, digite outro Código!");

                }else{
                    jtxtCodigo.setEditable(false);

                    jtxNome.setEnabled(true);
                    jcClasse.setEnabled(true);
                    jtxtPrecoVenda.setEnabled(true);
                    jtxtPrecoCusto.setEnabled(true);
                    jComboBox1.setEnabled(true);

                }
            } catch (SQLException ex) {
                Logger.getLogger(CadastroCliente.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_jtxtCodigoKeyPressed

    private void jbPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbPesquisarActionPerformed
        // TODO add your handling code here:
        int codigo = Integer.parseInt(jtxtCodigo.getText());

        String sql = "SELECT *FROM produto WHERE codigo = "+codigo;

        Conexao con = new Conexao();
        try {
            jtxtCodigo.setEditable(false);
            ResultSet retorno = con.sentenca.executeQuery(sql);
            while(retorno.next()){
                jtxNome.setText(retorno.getString("nome"));
                jcClasse.setSelectedItem(retorno.getString("classe"));
                jComboBox1.setSelectedItem(retorno.getString("fornecedor"));
                jtxtPrecoCusto.setText(String.valueOf(retorno.getFloat("precocusto")));
                jtxtPrecoVenda.setText(String.valueOf(retorno.getFloat("precovenda")));
                jtxNome.setEnabled(true);
                jcClasse.setEnabled(true);
                jComboBox1.setEnabled(true);
                jtxtPrecoVenda.setEnabled(true);
                jtxtPrecoCusto.setEnabled(true);
            }

        } catch (SQLException ex) {
            System.out.println("Erro ao Pesquisar!"+ex.getMessage());
        }
    }//GEN-LAST:event_jbPesquisarActionPerformed

