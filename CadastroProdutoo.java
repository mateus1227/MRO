    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed

        int codigo = Integer.parseInt(jtxtCodigo.getText());
        String nome = jtxNome.getText();
        String classe = (String) jcClasse.getSelectedItem();
        float precocusto = Float.parseFloat(jtxtPrecoCusto.getText());
        float precovenda = Float.parseFloat(jtxtPrecoVenda.getText());
        String fornecedor = (String) jComboBox1.getSelectedItem();
        String sql = "INSERT INTO produto (codigo , nome , classe ,fornecedor, precocusto, precovenda) VALUES ( "+codigo+",'"+nome+"','"+classe+"','"+fornecedor+"',"+precocusto+","+precovenda+" )";
        jtxtCodigo.setEditable(false);

        Conexao con = new Conexao();

        try {

            con.sentenca.executeUpdate(sql);

            JOptionPane.showMessageDialog(this, "Produto cadastrado com sucesso!");

            CadastroProdutoo tela = new CadastroProdutoo();
            tela.setVisible(true);dispose();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Erro no cadastro!");
        }

    }//GEN-LAST:event_jbSalvarActionPerformed

