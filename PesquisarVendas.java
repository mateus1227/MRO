     public void soma(){
        float totalvendido=0;
        Conexao con = new Conexao();
        String codigo = (String)jComboBox1.getSelectedItem();
        String sqll = "SELECT total FROM vendas WHERE codigovenda ='"+codigo+"'";
        
        try {
            ResultSet retorno = con.sentenca.executeQuery(sqll);
            while(retorno.next()){
                
            totalvendido=totalvendido+retorno.getFloat("total");
            }jtxtTotalVenda.setText(String.valueOf(totalvendido));
            
        } catch (SQLException ex) {
            Logger.getLogger(Vendas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
