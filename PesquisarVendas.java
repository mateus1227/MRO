    private void jbPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbPesquisarActionPerformed
        String sql = "SELECT data,nome,qtd, precovenda, total, cliente FROM vendas WHERE codigovenda like'%"+jComboBox1.getSelectedItem()+"%'";
        DefaultTableModel val = new DefaultTableModel(new Object[]{"Data Venda","Cliente","Produto","Quantidade","Preço Uitário R$", "Preço Total R$"},0);

        Conexao con = new Conexao();
        try {
            ResultSet retorno = con.sentenca.executeQuery(sql);
            
            while(retorno.next()){
                String client = retorno.getString("cliente");
                String dat = retorno.getString("data");
                String qua = retorno.getString("qtd");
                String nome = retorno.getString("nome");
                String precovenda = retorno.getString("precovenda");
                String precotota = retorno.getString("total");
                val.addRow(new String[] { dat,client, nome,qua, precovenda,precotota});
            }
            jtbPesquisavendas.setModel(val);
            soma();
        } catch (SQLException ex) {
            Logger.getLogger(Vendas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jbPesquisarActionPerformed

