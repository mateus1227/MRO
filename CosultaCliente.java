    private void clienteKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_clienteKeyReleased
        // TODO add your handling code here:
        String sql = "SELECT codigo, nome, telefone, endereco,email FROM cliente WHERE nome like'%"+cliente.getText()+"%'";
       DefaultTableModel val = new DefaultTableModel(new Object[]{"Código", "Cliente", "Telefone","Endereço", "Email"},0);
       
       Conexao con = new Conexao();
        try {
            ResultSet retorno = con.sentenca.executeQuery(sql);
            
            while(retorno.next()){
                
                String codigo = retorno.getString("codigo");
                String nome = retorno.getString("nome");
                String precovenda = retorno.getString("telefone");
                String precocusto = retorno.getString("endereco");
                String fornecedor = retorno.getString("email");
                  val.addRow(new String[] {codigo, nome, precovenda,precocusto,fornecedor});
            }
            jTable1.setModel(val);
            
        } catch (SQLException ex) {
            Logger.getLogger(Vendas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_clienteKeyReleased

